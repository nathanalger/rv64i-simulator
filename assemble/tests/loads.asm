# ----------------------------------------
# Load Instruction Test (RV64I)
# ----------------------------------------

# Base address
addi x1, x0, 100        # x1 = 100

# ----------------------------------------
# Store test values into memory
# ----------------------------------------

addi x2, x0, -1         # x2 = 0xFFFFFFFFFFFFFFFF
sw x2, 0(x1)            # store word at [100]

addi x3, x0, 127        # 0x7F
sw x3, 4(x1)

addi x4, x0, 128        # 0x80
sw x4, 8(x1)

# ----------------------------------------
# LB (signed byte)
# ----------------------------------------
lb x10, 0(x1)           # expect -1
lb x11, 4(x1)           # expect 127
lb x12, 8(x1)           # expect -128

# ----------------------------------------
# LBU (unsigned byte)
# ----------------------------------------
lbu x13, 0(x1)          # expect 255
lbu x14, 4(x1)          # expect 127
lbu x15, 8(x1)          # expect 128

# ----------------------------------------
# LH (signed half)
# ----------------------------------------
lh x16, 0(x1)           # expect -1
lh x17, 4(x1)           # expect 127
lh x18, 8(x1)           # expect 128

# ----------------------------------------
# LHU (unsigned half)
# ----------------------------------------
lhu x19, 0(x1)          # expect 65535
lhu x20, 4(x1)          # expect 127
lhu x21, 8(x1)          # expect 128

# ----------------------------------------
# LW (signed word)
# ----------------------------------------
lw x22, 0(x1)           # expect -1
lw x23, 4(x1)           # expect 127
lw x24, 8(x1)           # expect 128

# ----------------------------------------
# LWU (unsigned word)
# ----------------------------------------
lwu x25, 0(x1)          # expect 0xFFFFFFFF (4294967295)
lwu x26, 4(x1)          # expect 127
lwu x27, 8(x1)          # expect 128

# ----------------------------------------
# LD (64-bit load)
# ----------------------------------------
ld x28, 0(x1)           # loads full 64 bits
ld x29, 4(x1)
ld x30, 8(x1)

# End (infinite loop)
jal x0, 0